    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (validarDat())
        {
            if (bdpro.verificarProducto(Nombre))
            {
                JOptionPane.showMessageDialog(this, "El producto ya existe", "Advertencia",2);
            }
            else
            {
                if(Codigo == 0)
                {
                    int codAux = bdpro.ultCod() + 1;
                    bdpro.Agregar(new Producto(codAux, Nombre, Precio, Inventario));
                    this.tblTabla.setModel(new tablaModeloP(bdpro.obtenerLP()));
                    limpiarDat();
                    JOptionPane.showMessageDialog(this, "Producto agregado exitosamente", "ConfirmaciÃ³n",1);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Ãsta opciÃ³n es solo para actualizar o borrar", "Advertencia",2);
                }
                
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Alguno de los campos estÃ¡ vacio o es erroneo", "Advertencia",2);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tblTablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTablaMouseClicked
        Producto producto = ((tablaModeloP) this.tblTabla.getModel()).detalle(this.tblTabla.getSelectedRow());
        this.txtCodigo.setText(String.valueOf(producto.getCodigo()));
        this.txtNombre.setText(producto.getNombre());
        this.txtPrecio.setText(String.valueOf(producto.getPrecio()));
        this.txtInventario.setText(String.valueOf(producto.getInventario()));
    }//GEN-LAST:event_tblTablaMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (validarDat())
        {
            bdpro.Actualizar(new Producto(Codigo, Nombre, Precio, Inventario));
            this.tblTabla.setModel(new tablaModeloP(bdpro.obtenerLP()));
            limpiarDat();
            JOptionPane.showMessageDialog(this, "Producto actualizado exitosamente", "ConfirmaciÃ³n",1);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Alguno de los campos estÃ¡ vacio o es erroneo", "Advertencia",2);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (validarDat())
        {
            bdpro.Eliminar(new Producto(Codigo, Nombre, Precio, Inventario));
            this.tblTabla.setModel(new tablaModeloP(bdpro.obtenerLP()));
            limpiarDat();
            JOptionPane.showMessageDialog(this, "Producto eliminado exitosamente", "ConfirmaciÃ³n",1);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Alguno de los campos estÃ¡ vacio o es erroneo", "Advertencia",2);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

